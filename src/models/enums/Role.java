package models.enums;

/**
 * Enum representing different user roles in the system.
 */
public enum Role {
    /**
     * Represents staff doctor role, responsible for patient care and treatment.
     */
    DOCTOR,

    /**
     * Represents staff pharmacist role, responsible for dispensing medications.
     */
    PHARMACIST,

    /**
     * Represents staff administrator role, responsible for managing system operations.
     */
    ADMINISTRATOR,

    /**
     * Represents a patient role, an individual receiving medical care.
     */
    PATIENT
}
